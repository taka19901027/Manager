package jp.co.techtone.nsm.form;

public class PriceInsertForm {

	private String YEAR;

	private String MONTH;

	private String DAY;

	private String OPEN_INSERT;

	private String HIGH_INSERT;

	private String LOW_INSERT;

	private String CLOSE_INSERT;

	public String getYEAR() {
		return YEAR;
	}

	public void setYEAR(String yEAR) {
		YEAR = yEAR;
	}

	public String getMONTH() {
		return MONTH;
	}

	public void setMONTH(String mONTH) {
		MONTH = mONTH;
	}

	public String getDAY() {
		return DAY;
	}

	public void setDAY(String dAY) {
		DAY = dAY;
	}

	public String getOPEN_INSERT() {
		return OPEN_INSERT;
	}

	public void setOPEN_INSERT(String oPEN_INSERT) {
		OPEN_INSERT = oPEN_INSERT;
	}

	public String getHIGH_INSERT() {
		return HIGH_INSERT;
	}

	public void setHIGH_INSERT(String hIGH_INSERT) {
		HIGH_INSERT = hIGH_INSERT;
	}

	public String getLOW_INSERT() {
		return LOW_INSERT;
	}

	public void setLOW_INSERT(String lOW_INSERT) {
		LOW_INSERT = lOW_INSERT;
	}

	public String getCLOSE_INSERT() {
		return CLOSE_INSERT;
	}

	public void setCLOSE_INSERT(String cLOSE_INSERT) {
		CLOSE_INSERT = cLOSE_INSERT;
	}


}

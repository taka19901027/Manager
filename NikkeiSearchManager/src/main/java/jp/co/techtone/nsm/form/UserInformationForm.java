package jp.co.techtone.nsm.form;

public class UserInformationForm {

	private String USER_ID;

	private String USER_PERMISSION;

	private int USER_MONEY;

	public String getUSER_ID() {
		return USER_ID;
	}

	public void setUSER_ID(String uSER_ID) {
		USER_ID = uSER_ID;
	}

	public String getUSER_PERMISSION() {
		return USER_PERMISSION;
	}

	public void setUSER_PERMISSION(String uSER_PERMISSION) {
		USER_PERMISSION = uSER_PERMISSION;
	}

	public int getUSER_MONEY() {
		return USER_MONEY;
	}

	public void setUSER_MONEY(int uSER_MONEY) {
		USER_MONEY = uSER_MONEY;
	}

}
